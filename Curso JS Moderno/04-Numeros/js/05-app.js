// Numbers - incrementos y decrementos
let puntaje = 5;

// Incremento ++ --> Imprime y luego aumenta en 1;
console.log(puntaje++); // 5
console.log(puntaje); // 6

// ++ Incremento --> Aumenta en 1 y luego imprime;
console.log(++puntaje); // 7


let puntaje2 = 5;
// Decremento -- --> Imprime y luego disminuye en 1;
console.log(puntaje2--);//5
console.log(puntaje2);//4

// -- Decremento --> Disminuye en 1 y luego imprime;
console.log(--puntaje2);//3

// Aumentar en mayor cantidad
let puntaje3 = 5;

// puntaje3 = puntaje3 + 3;
puntaje3 += 3;
console.log(puntaje3); // 5 + 3 = 8

// Disminuir en mayor cantidad
puntaje3 -= 4; // 8 - 4 = 4
console.log(puntaje3);