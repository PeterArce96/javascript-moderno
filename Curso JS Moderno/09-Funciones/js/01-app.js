// Funciones - Crear una funcion, Function expression y function declaration

// Las funciones son una serie de instrucciones o línea de código que realizan una acción, permiten un código más ordenado y facil de mantener, son reutilizables.

// Function declaration
function sumar() {
    console.log(2 + 2);
}
// llamar a la función
sumar(); //4

// Function expression
const sumar2 = function() {
    console.log(3 + 3);
}
sumar2();//6